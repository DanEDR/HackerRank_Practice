import numpy as np

if __name__ == '__main__':
  shape = list(map(int, input().split()))
  
  matrix_zeros = np.array(np.zeros(shape, dtype=int))
  matrix_ones = np.array(np.ones(shape, dtype=int))

  print(matrix_zeros)
  print(matrix_ones)
